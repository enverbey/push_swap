#include "../../include/push_swap.h"
#include <stdlib.h>

void	free_array(char **s2)
{
	int	i;

	i = 0;
	while (s2[i])
		i++;
	i--;
	while (i >= 0)
	{
		free(s2[i]);
		i--;
	}
}
